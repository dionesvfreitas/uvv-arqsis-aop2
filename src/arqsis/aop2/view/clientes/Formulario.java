    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Cliente cliente = new Cliente();
        cliente.setMatricula(Integer.parseInt(txtMatricula.getText()));
        cliente.setNome(txtNome.getText());
        cliente.setSobreNome(txtSobrenome.getText());
        cliente.setEmail(txtEmail.getText());
        cliente.setDataDeNascimento(txtDataNascimento.getText());
        cliente.setCpf(txtCPF.getText());
        cliente.setIdade(txtIdade.getText().trim().length() == 0 ? 1 : Integer.parseInt(txtIdade.getText()));
        cliente.setProfissao(txtProfissao.getText());
        cliente.setSenha(txtSenha.getSelectedText());
        this.controller.save(cliente);
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed


