    private void loadForm(Cliente cliente) {
        if (cliente != null) {
            this.txtMatricula.setText(Integer.toString(cliente.getMatricula()));
            this.txtCPF.setText(cliente.getCpf());
            this.txtDataNascimento.setText(cliente.getDataDeNascimento());
            this.txtEmail.setText(cliente.getEmail());
            this.txtIdade.setText(Integer.toString(cliente.getIdade()));
            this.txtNome.setText(cliente.getNome());
            this.txtSobrenome.setText(cliente.getSobreNome());
            this.txtProfissao.setText(cliente.getProfissao());
            this.txtSenha.setText(cliente.getSenha());
        }
    }
